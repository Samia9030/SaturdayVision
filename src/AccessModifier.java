
public class AccessModifier {

	public static void main(String[] args) {
		
		//  public - Any class can access
		
		// protected - Classes in the same package and subclass
		
		// default - classes in same package can access
		
		// private - Only same class can access
		
		public String name = "Upskill";   //  public - Any class can access
		
		protected int ssn = 986453452;    // protected - Classes in the same package and subclass
	    
	    private int age = 31;             // private - Only same class can access
		
		default String address = "Heritage Tower";   // default - classes in same package can acces
		
		
				

	}

}
